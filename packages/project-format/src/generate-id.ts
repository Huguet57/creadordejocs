/**
 * Generate a UUID v4 string.
 *
 * Uses `crypto.randomUUID()` when available (secure contexts / HTTPS).
 * Falls back to a `crypto.getRandomValues()` implementation so the app
 * still works over plain HTTP (e.g. during local dev or before SSL is ready).
 */
export function generateUUID(): string {
  if (typeof crypto !== "undefined" && typeof crypto.randomUUID === "function") {
    return crypto.randomUUID()
  }
  return uuidV4Fallback()
}

function uuidV4Fallback(): string {
  const bytes = new Uint8Array(16)
  crypto.getRandomValues(bytes)

  // Set version 4 (0100) in byte 6
  bytes[6] = (bytes[6]! & 0x0f) | 0x40
  // Set variant 10xx in byte 8
  bytes[8] = (bytes[8]! & 0x3f) | 0x80

  const hex = Array.from(bytes, (b) => b.toString(16).padStart(2, "0")).join("")
  return `${hex.slice(0, 8)}-${hex.slice(8, 12)}-${hex.slice(12, 16)}-${hex.slice(16, 20)}-${hex.slice(20)}`
}
